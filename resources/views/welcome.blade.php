        $("#submit").on('click', function () {

            let email = $('#email').val();
            let password = $('#password').val();

            $.ajaxSetup({
                headers: {

                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });

            $.ajax({
                type: "POST",
                url: "{{ url('sample') }}",
                data: {
                    email: email,
                    password: password
                },
                success: function (result) {
                    console.log(result);
                    alert(`email : ${result.email} , password : ${result.password}`)
                }
            })
        })


